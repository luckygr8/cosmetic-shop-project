    public void bindTableDetails() {
        allcategories = new Categoryinfo_DAO().getallrecords();
        String[] col_names = {"Category ID", "Category Name"};
        Object[][] records = null;
        if (allcategories != null && allcategories.size() > 0) {
            records = new Object[allcategories.size()][col_names.length];
            int i = 0;
            for (Categoryinfo_TO ct : allcategories) {
                records[i] = new Object[]{ct.getCategoryid(), ct.getCategoryname()};
                i++;
            }
        } else {
            records = new Object[1][col_names.length];
            records[0] = new Object[]{"No Records", "No Records"};
        }
        DefaultTableModel dtm = new DefaultTableModel(records, col_names) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        tbl.setModel(dtm);
    }
