    private void deleteRecord() {
        if (srow != -1 && allcategories != null && srow < allcategories.size()) {
            int result = JOptionPane.showConfirmDialog(this, "Are You Sure To Removed This Reccord?", "Message From System", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);
            if (result == JOptionPane.YES_OPTION) {
                Categoryinfo_TO ci = allcategories.get(srow);
                Categoryinfo_DAO action = new Categoryinfo_DAO();
                String message = "";
                if (action.deleterecord(ci.getCategoryid())) {
                    message = "Record is Removed From Database";
                    bindTableDetails();
                } else {
                    message = Error_messages.ERROR_ON_DELETE_A_BRAND_WHILE_IT_IS_IN_SUB_BRAND;
                }
                JOptionPane.showMessageDialog(this, message);
            }
        }
        srow = -1;
    }

    private void editRecord() {
        if (srow != -1 && allcategories != null && srow < allcategories.size()) {
            Checks.openInternalFrame(Mainscreen.getDeskpane(), new Input_category(2));
            dispose();
        }
    }
    
